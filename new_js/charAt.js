/*
this is Char At example in javascript
*/


//Example of char at
var str = "Hello Geekyshows";
document.write(str.charAt(9) + "<br>");

//Example if charCodeAt
var str1 = "Aminur";
document.write(str1.charCodeAt(3) + "<br>");

//Example of toUppercase method
var str2 = "Bangladesh";
document.write(str2.toUpperCase(str2)+"<br>");
document.write(str2.toLowerCase(str2) + "<br>");


//Trim Method
var str3 = "           Hello Bangladesh          ";
document.write(str3.trim() + "<br>");
console.log(str3.trim());


//replace method
var str4 = "Hello Geekyshows";
document.write(str4.replace("Geekyshows","World"));